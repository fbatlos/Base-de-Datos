  COD_AL		NUMBER(2)	PRIMARY KEY,
  FECINC_AL		DATE,
  FECNAC_AL		DATE,
  DNI_AL		VARCHAR2(9)		UNIQUE,
  NOMBRE_AL		VARCHAR2(25) 	NOT NULL,
  CIUDAD_AL		VARCHAR2(10) 	NOT NULL,
  DELEGADO_AL	NUMBER(2)
  );

